function sum(a:number, b:number): number{
    return a+b;
}

const ans = sum(2,3)
console.log(ans);

// a,b: number → parameter with a type

// : number after the parenthesis → return type of the function

function greet(name: string): string {
  return "Hello, " + name;
}

console.log(greet("Razz")); // Output: Hello, Razz

// name: string → parameter with a type

// : string after the parenthesis → return type of the function
